  handleInput(direction) {
    let [newX, newY] = this.state.playerPosition;
    
    switch (direction) {
      case 'up':
        newY--;
        break;
      case 'down':
        newY++;
        break;
      case 'left':
        newX--;
        break;
      case 'right':
        newX++;
        break;
      default:
        return;
    }

    if (this.canMove(newX, newY)) {
      this.movePlayer(newX, newY);  
    }
    else {
      switch(this.state.map[newY][newX]) {
        case 3:
        case 4:
        case 5:
        case 6:
        case 7:
        case 8:
          this.fightEnemy(newX, newY, this.state.map[newY][newX]);
          break;
        case 9:
          this.getPotion(newX, newY)
          break;
        case 10:
          this.getItem(newX, newY);
          break;
        default:
          return;
      }
    }
    
  }

