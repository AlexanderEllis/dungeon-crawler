class Board extends Component {
  render() {
    let map = this.props.map;
    let displaying = [];
    let mapping = ['wall', 'space', 'player', 'enemy', 'enemy', 'enemy', 'enemy', 'enemy', 'boss', 'potion', 'item', 'treasure'];
    let [playerX, playerY] = this.props.playerPosition;
    for (let i = 0; i < map.length; i++) {
      for (let j = 0; j < map[0].length; j++) {
        let type = 'dark'; 
        
        if (i === playerY - 3 || i === playerY + 3) {
          if (j === playerX) {
            type = mapping[map[i][j]];
          }
        }
        else if (i === playerY - 2 || i === playerY + 2) {
          if (j > playerX - 2 && j < playerX + 2) {
            type = mapping[map[i][j]];
          }
        }
        else if (i === playerY - 1 || i === playerY + 1) {
          if (j > playerX - 3 && j < playerX + 3) {
            type = mapping[map[i][j]];
          }
        }
        else if (i === playerY) {
          if (j > playerX - 4 && j < playerX + 4) {
            type = mapping[map[i][j]];
          }
        }
        
        displaying.push(<div className={type + ' tile'} />)
      }
    }
    return (
      <div id="board">
        {displaying}
      </div>
    )
  }
}

